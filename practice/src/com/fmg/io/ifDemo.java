package com.fmg.io;

public class ifDemo {

	public static void main(String args[]) {
		String b = "10";
		if("10".equals(b)){
			System.out.println("1111111111");
		}else if(!b.equals("1")){
			System.out.println("2222222222222");
		}else{
			System.out.println("33333333333");
		}
	}

}
